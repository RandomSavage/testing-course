export function add(a, b) {
  if(a === undefined || b === undefined) {
    return null
  }
  return a + b
}

export function minus(a, b){
  if(a === undefined || b === undefined) {
    return null
  }
  return a - b
}

export function multiply(a, b) {
  if(a === undefined || b === undefined) {
    return null
  }
  return a * b
}